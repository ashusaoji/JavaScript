//sum 
const arrowSum = (a, b) => {
    return a + b;
}

let a = 10;
let b = 15;

console.log(arrowSum(a, b));

//substract

const arrowSub = (a, b) => {
    return a - b;
}

console.log(arrowSub(a, b));

//Multiplication

const arrowMult = (a, b) => {
    return a * b;
}

console.log(arrowMult(a, b));

// Division

const arrowDiv = (a, b) => {
    return a / b;
}

console.log(arrowDiv(a, b));